package com.springproj.ex01;

public class SSTV {
	
	public void powerOn() {
		System.out.println("Tv가 켜집니다.");
	}
	public void powerOff() {
		System.out.println("Tv가 꺼집니다.");
	}
	public void volumeUp() {
		System.out.println("소리가 1 증가합니다.");
	}
	public void volumeDown() {
		System.out.println("소리가 1 감소합니다.");
	}

}
